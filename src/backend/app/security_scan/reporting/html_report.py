        "      background-image: linear-gradient(var(--table-col-alt), var(--table-col-alt));\n"
        "    }\n"
        "    th:last-child, td:last-child {\n"
        "      border-right: none;\n"
        "    }\n"
        "    tbody tr:last-child td {\n"
        "      border-bottom: none;\n"
        "    }\n"
        "    tbody tr:nth-child(even) td {\n"
        "      background-color: #e9eef5 !important;\n"
        "      background-color: var(--table-row-alt) !important;\n"
        "    }\n"
        "    tbody td:nth-child(even) {\n"
        "      background-image: linear-gradient(rgba(2, 6, 23, 0.10), rgba(2, 6, 23, 0.10)) !important;\n"
        "      background-image: linear-gradient(var(--table-col-alt), var(--table-col-alt)) !important;\n"
