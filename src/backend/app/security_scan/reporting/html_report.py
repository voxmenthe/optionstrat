        "<!doctype html>\n"
        "<html lang=\"en\">\n"
        "<head>\n"
        "  <meta charset=\"utf-8\" />\n"
        "  <meta name=\"viewport\" content=\"width=device-width, initial-scale=1\" />\n"
        "  <title>Security Scan Report</title>\n"
        "  <style>\n"
        "    :root {\n"
        "      color-scheme: light;\n"
        "      --bg: #ffffff;\n"
        "      --text: #0f172a;\n"
        "      --muted: #475569;\n"
        "      --surface: #ffffff;\n"
        "      --surface-muted: #f8fafc;\n"
        "      --border: #cbd5e1;\n"
        "      --border-strong: #94a3b8;\n"
        "      --code-bg: #f1f5f9;\n"
        "      --table-header-bg: #e2e8f0;\n"
        "      --table-row-alt: #e9eef5;\n"
        "      --table-col-alt: rgba(2, 6, 23, 0.10);\n"
        "      --table-hover: rgba(59, 130, 246, 0.06);\n"
        "    }\n"
        "    body {\n"
        "      margin: 24px;\n"
        "      font-family: ui-sans-serif, system-ui, -apple-system, \"Segoe UI\", Roboto, Helvetica, Arial, sans-serif;\n"
        "      font-size: 16px;\n"
        "      line-height: 1.45;\n"
        "      color: var(--text);\n"
        "      background: var(--bg);\n"
        "    }\n"
        "    .report {\n"
        "      max-width: 1100px;\n"
        "      margin: 0 auto;\n"
        "    }\n"
        "    h1, h2, h3 {\n"
        "      margin-top: 1.4rem;\n"
        "      letter-spacing: -0.01em;\n"
        "    }\n"
        "    h1 {\n"
        "      margin-top: 0;\n"
        "    }\n"
        "    table {\n"
        "      border-collapse: separate;\n"
        "      border-spacing: 0;\n"
        "      width: 100%;\n"
        "      margin-bottom: 1.5rem;\n"
        "      font-size: 0.95rem;\n"
        "      background: var(--surface);\n"
        "      border: 1px solid var(--border-strong);\n"
        "      border-radius: 10px;\n"
        "      overflow: hidden;\n"
        "      box-shadow: 0 1px 0 rgba(15, 23, 42, 0.04);\n"
        "    }\n"
        "    th, td {\n"
        "      padding: 10px 12px;\n"
        "      text-align: left;\n"
        "      white-space: normal;\n"
